    async def phase_0a_legal_training(self):
        """
        Phase 0A: Deep legal training to become expert arbitration litigator
        Actually reads and analyses legal documents to build expertise
        """
        legal_dir = self.project_root / "legal_resources"
        
        print("📚 Building deep legal expertise...")
        
        # Load all legal documents
        legal_texts = []
        legal_docs = []
        
        if legal_dir.exists():
            for file in legal_dir.glob("**/*"):
                if file.suffix in ['.txt', '.pdf', '.doc', '.docx']:
                    legal_docs.append({
                        'doc_id': file.stem,
                        'filename': file.name,
                        'path': str(file)
                    })
                    # Try to read text files directly
                    if file.suffix == '.txt':
                        try:
                            with open(file, 'r', encoding='utf-8') as f:
                                content = f.read()
                                legal_texts.append(f"[{file.name}]\n{content[:5000]}")
                        except Exception as e:
                            logger.error(f"Error reading {file}: {e}")
        
        # Get legal training prompt from prompts module
        legal_training_prompt = prompts.get_phase_0a_legal_training_prompt(
            doc_count=len(legal_docs),
            legal_texts=legal_texts[:10]  # First 10 documents
        )
        
        # Send to API for learning
        response = await self.api_client.make_api_call(legal_training_prompt, phase='phase_0a')
        
        # Store comprehensive legal knowledge
        self.knowledge_base['legal_training'] = {
            'expertise_summary': response,
            'documents_studied': len(legal_docs),
            'training_complete': True,
            'specialisations': [
                'LCIA Arbitration',
                'Document Forensics', 
                'Fraud Detection',
                'Cross-Examination',
                'Adverse Inference'
            ],
            'key_precedents': {
                'P&ID': 'Fraud detection methods',
                'Seamus Andrew': 'Failed defence patterns',
                'Document withholding': 'Adverse inference triggers'
            },
            'timestamp': datetime.now().isoformat()
        }
        
        # Store phase findings
        self.phase_findings['phase_0a'] = response
        
        print(f"✅ Legal expertise acquired from {len(legal_docs)} sources")
        self.investigator._save_knowledge_base()
    
