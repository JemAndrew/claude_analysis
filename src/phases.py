    async def phase_1_landscape(self, documents: List[Dict]):
        """
        Phase 1: Document landscape with full legal/case knowledge applied
        """
        # Get accumulated knowledge
        legal_knowledge = self.knowledge_base.get('legal_training', {}).get('expertise_summary', '')
        case_strategy = self.knowledge_base.get('case_context', {}).get('strategic_analysis', '')
        
        # Get enhanced phase 1 prompt
        base_prompt = prompts.get_phase_1_prompt_enhanced(
            legal_knowledge=legal_knowledge[:2000],
            case_strategy=case_strategy[:2000]
        )
        
        # Build cumulative context
        cumulative_context = self._build_cumulative_context(1)
        
        # Process documents in batches
        batch_size = self.config.get('investigation', {}).get('batch_size', 40)
        batches = utils.batch_documents(documents, batch_size)
        
        all_findings = []
        
        for batch_num, batch in enumerate(batches):
            # Combine prompts
            full_prompt = f"""
            {base_prompt}
            
            {cumulative_context}
            
            Batch {batch_num + 1}/{len(batches)}: Apply Opus 4.1 forensic capabilities.
            """
            
            # Add documents
            formatted_batch = utils.format_batch_with_metadata(batch)
            final_prompt = full_prompt + "\n\nDocuments:\n" + formatted_batch
            
            # Send to API
            response = await self.api_client.make_api_call(final_prompt, phase='phase_1')
            all_findings.append(response)
            
            # Track documents for withholding analysis
            for doc in batch:
                self.document_tracker.analyse_for_withholding(
                    doc.get('text', ''),
                    doc.get('doc_id', 'unknown')
                )
            
            # Update knowledge base
            self._update_knowledge_base('phase_1', response)
        
        # Store all phase findings
        self.phase_findings['phase_1'] = "\n\n".join(all_findings)
        
        # Generate dynamic prompt for next phase
        await self._generate_dynamic_next_phase_prompt(1)
        
        self.investigator.performance_metrics['phases_completed'] += 1
        print("✅ Phase 1 complete: Forensic landscape analysis completed")
    
