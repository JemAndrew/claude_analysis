    async def _generate_dynamic_next_phase_prompt(self, completed_phase: int):
        """
        Claude generates its own investigation strategy for the next phase
        """
        if completed_phase >= 6:
            return  # No next phase after 6
        
        # Get findings from completed phase
        phase_findings = self.phase_findings.get(f'phase_{completed_phase}', '')
        
        # Get prompt for dynamic generation
        dynamic_prompt = prompts.get_dynamic_phase_prompt(
            completed_phase=completed_phase,
            phase_findings=phase_findings[:3000],
            accumulated_knowledge=self._get_accumulated_knowledge()[:2000]
        )
        
        # Get Claude's self-generated investigation instructions
        response = await self.api_client.make_api_call(
            dynamic_prompt, 
            phase=f'dynamic_{completed_phase}'
        )
        
        # Store for next phase
        self.dynamic_prompts[f'phase_{completed_phase + 1}'] = response
        
        logger.info(f"Generated dynamic prompt for Phase {completed_phase + 1}")
    
    async def _correlate_kill_shots_across_batches(self, findings: str, batch_num: int):
        """
        Correlate kill shots across all batches
        """
        previous_kill_shots = json.dumps(
            self.knowledge_base.get('kill_shots', {}), 
            indent=2
        )[:2000]
        
        correlation_prompt = prompts.get_correlation_prompt(
            batch_num=batch_num,
            current_findings=findings[:2000],
            previous_kill_shots=previous_kill_shots
        )
        
        response = await self.api_client.make_api_call(
            correlation_prompt, 
            phase='correlation'
        )
        
        # Store correlation findings
        if 'forensic_findings' not in self.investigator.investigation_memory:
            self.investigator.investigation_memory['forensic_findings'] = []
        
        self.investigator.investigation_memory['forensic_findings'].append({
            'batch': batch_num,
            'correlations': response,
            'timestamp': datetime.now().isoformat()
        })
    
    async def _rank_kill_shots_for_tribunal_impact(self, all_kill_shots: List[str]):
        """
        Final ranking for maximum tribunal impact
        """
        ranking_prompt = prompts.get_final_ranking_prompt(
            all_kill_shots=str(all_kill_shots)[:12000]
        )
        
        response = await self.api_client.make_api_call(
            ranking_prompt, 
            phase='final_ranking'
        )
        
        # Store final ranking
        if 'kill_shots' not in self.investigator.investigation_memory:
            self.investigator.investigation_memory['kill_shots'] = []
        
        self.investigator.investigation_memory['kill_shots'].append({
            'final_ranking': response,
            'timestamp': datetime.now().isoformat(),
            'model': 'opus_4.1_final'
        })
        
        # Save the final ranking
        output_file = self.project_root / "outputs" / "00_OPUS_41_FINAL_KILL_SHOT_RANKING.md"
        output_file.parent.mkdir(parents=True, exist_ok=True)
        with open(output_file, 'w', encoding='utf-8') as f:
            f.write(f"# OPUS 4.1 FINAL KILL SHOT RANKING\n\n{response}")
        
        print(f"✅ Final Kill Shot Ranking saved to: {output_file}")
    
    def _build_cumulative_context(self, phase: int) -> str:
        """
        Build context with all accumulated knowledge
        """
        context = "OPUS 4.1 ACCUMULATED INTELLIGENCE:\n\n"
        
        # Add legal training
        if self.knowledge_base.get('legal_training'):
            context += "Legal Framework Mastery:\n"
            context += f"{json.dumps(self.knowledge_base['legal_training'], indent=2)[:1000]}\n\n"
        
        # Add case context
        if self.knowledge_base.get('case_context'):
            context += "Case Intelligence:\n"
            context += f"{json.dumps(self.knowledge_base['case_context'], indent=2)[:1000]}\n\n"
        
        # Add previous phase findings
        for i in range(1, phase):
            if f'phase_{i}' in self.phase_findings:
                context += f"Phase {i} Findings:\n"
                context += f"{self.phase_findings[f'phase_{i}'][:500]}\n\n"
        
        return context[:5000]  # Limit total context size
    
    def _update_knowledge_base(self, phase: str, findings: str):
        """
        Update knowledge base with phase findings
        """
        # Store raw findings
        if phase not in self.knowledge_base:
            self.knowledge_base[phase] = []
        
        self.knowledge_base[phase].append({
            'findings': findings[:2000],  # Store sample
            'timestamp': datetime.now().isoformat()
        })
        
        # Save to disk
        self.investigator._save_knowledge_base()
    
    def _get_accumulated_knowledge(self) -> str:
        """
        Get summary of all accumulated knowledge
        """
        knowledge = {
            'legal_expertise': self.knowledge_base.get('legal_training', {}).get('expertise_summary', '')[:500],
            'case_strategy': self.knowledge_base.get('case_context', {}).get('strategic_analysis', '')[:500],
            'patterns_found': len(self.knowledge_base.get('patterns', {}).get('control_patterns', [])),
            'anomalies_detected': len(self.knowledge_base.get('anomalies', {})),
            'theories_developed': len(self.knowledge_base.get('theories', {}))
        }
        
        return json.dumps(knowledge, indent=2)